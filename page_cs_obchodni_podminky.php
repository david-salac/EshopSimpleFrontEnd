    <h2>Obecná ustanovení</h2>
    <p>Obchodní podmínky se řídí platnou legislativou v České republice. Na eshopu mohou nakupovat pouze podnikající fyzické osoby (OSVČ) nebo právnické osoby (souhrnně výhradně osoby, které mají IČO), tedy nikoli spotřebitelé.</p>
    <h2 id="navObjZboz">Objednávka zboží</h2>
    <p>Potvrzením objednávky vybraného zboží vzniká pro objednávající osobu závazná objednávka a objednávající osoba tím vyjadřuje souhlas s obchodními podmínkami. Při zadávání objednávky vyplňuje objednávající povinně email, který slouží k odeslání potvrzení o přijetí objednávky a jiných relevantních emailů spojených zejména (ale ne výhradně) s expedicí objednaného zboží a zasláním platebních údajů prodávajícího.</p>
    <h2 id="navStornoObj">Stornování objednávky</h2>
    <p>Dokud není objednávka expadována, je možné ji písemně stornovat (postačí poslat email z mailové adresy uvedené při objednávání), bez toho, aniž by si prodávající mohl nárokovat uhrazení škody tímto úkonem vzniklé. Pokud je stornovnána objednávka ve stavu, kdy je již expadovaná, může si prodávající nárokovat vzniklou škodu (zejména, ale ne výhradně, úhradu nákladů spojených přepravními službami a balením zásilky). Prodávající může objednávku stornovat kdykoli a to i bez udání důvodu, tímto úkonem nevzniká kupujícímu v žádném případě nárok na úhradu případné škody a to vyjma případu, kdy byla objednávka již uhrazena, pak má kupující (výhradně) nárok na vrácení zaslaných peněz do 15 (slovy patnácti) pracovních dní.</p>
    <h2 id="navDodejZbozi">Dodávání zboží</h2>
    <p>Zboží je expedováno do doby uvedené v detailu každého produktu (standardně, ale ne výhradně, se jedná o čtyři pracovní dny). Celková doba doručení je pak především závislá na vybrané přepravní službě. Při platbě převodem na účet zboží expedujeme výhradně až po přijetí platby. Volbu dopravy provádí objednávající, cena dopravy je od této volby odvislá a je objednávajícímu známa již před potvrzením objednávky.</p>
    <h2 id="navMoznostiPlatby">Možnosti platby</h2>
    <p>Platit je možné buď při přebírání zboží dobírkou nebo předem bankovním převodem na účet prodávajícího, který je zasílán spolu potvrzením objednávky. U objednávek, jejichž souhrnná hodnota převyšuje 5 000,- Kč (slovy pět tisíc korun českých) je platba předem vyžadována.</p>
    <h2 id="navVraceniZbozi">Vrácení zboží a odstoupení od kupní smlouvy</h2>
    <p>Upozorňujeme, že kupující je vždy podnikatel a nemá tak spotřebitelskou ochranu. Zejména (ale ne výhradně) není možné vracet zboží bez udání důvodu a to ani ve lhůtě 14 dnů po jeho doručení. Prodávající odpovídá pouze za vady, které má věc v okamžiku převzetí. Pokud kupující uplatní reklamaci, vyřídí ji prodávající do jednoho roku od přijetí reklamace. Reklamace je možné podávat pouze písemně na adresu sídla prodávajícího.</p>
    <h2>Osobní data a jejich ochrana</h2>
    <p>Osobní data jsou ukládána především pro účely doručení objednávky zákazníkovi a pro účely řešení případných reklamací. Veškeré náležitosti spojené s nakládáním s osobními daty se řídí platnou legislativou.</p>
    
